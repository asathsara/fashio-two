class Item {
    constructor(images, name, price, stock, category, subCategory, sizes, description) {
        this.images = images;
        this.name = name;
        this.price = price;
        this.stock = stock;
        this.category = category;
        this.subCategory = subCategory;
        this.sizes = sizes;
        this.description = description;
    }
}

export default Item